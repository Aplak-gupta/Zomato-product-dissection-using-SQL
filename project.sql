--1.Total Users by City--
SELECT city, COUNT(*) AS total_users
FROM users
GROUP BY city
ORDER BY total_users DESC;

--2.Total Revenue Generated by Each Restaurant
SELECT r.name, SUM(o.total_amount) AS revenue
FROM orders o
JOIN restaurants r ON o.restaurant_id = r.restaurant_id
WHERE o.order_status = 'Delivered'
GROUP BY r.name
ORDER BY revenue DESC;

--3. Most Ordered Food Item
SELECT m.item_name, SUM(oi.quantity) AS total_ordered
FROM order_items oi
JOIN menu m ON oi.menu_id = m.menu_id
GROUP BY m.item_name
ORDER BY total_ordered DESC
LIMIT 1;

--4. Most Valuable Customers (Highest Spending)
SELECT u.name, SUM(o.total_amount) AS total_spent
FROM users u
JOIN orders o ON u.user_id = o.user_id
WHERE o.order_status = 'Delivered'
GROUP BY u.name
ORDER BY total_spent DESC;

--5. Order Status Distribution
SELECT order_status, COUNT(*) AS total_orders
FROM orders
GROUP BY order_status;

--6. Payment Method Usage
SELECT payment_method, COUNT(*) AS usage_count
FROM payments
WHERE payment_status = 'Success'
GROUP BY payment_method
ORDER BY usage_count DESC;

--7. Average Rating per Restaurant
SELECT r.name, AVG(rev.rating) AS avg_rating
FROM reviews rev
JOIN restaurants r ON rev.restaurant_id = r.restaurant_id
GROUP BY r.name
ORDER BY avg_rating DESC;

--8. Daily Revenue Trend
SELECT DATE(order_date) AS order_day,
       SUM(total_amount) AS daily_revenue
FROM orders
WHERE order_status = 'Delivered'
GROUP BY order_day
ORDER BY order_day;

--9. Average Order Value (AOV)
SELECT AVG(total_amount) AS avg_order_value
FROM orders
WHERE order_status = 'Delivered';

--10. Top 3 Restaurants by Number of Orders
SELECT r.name, COUNT(o.order_id) AS total_orders
FROM orders o
JOIN restaurants r ON o.restaurant_id = r.restaurant_id
GROUP BY r.name
ORDER BY total_orders DESC
LIMIT 3;

--1. Rank Restaurants by Revenue (WINDOW FUNCTION)
SELECT r.name,
       SUM(o.total_amount) AS revenue,
       RANK() OVER (ORDER BY SUM(o.total_amount) DESC) AS revenue_rank
FROM orders o
JOIN restaurants r ON o.restaurant_id = r.restaurant_id
WHERE o.order_status = 'Delivered'
GROUP BY r.name;

--2. Customer Order Frequency
SELECT u.name,
       COUNT(o.order_id) AS total_orders,
       CASE
           WHEN COUNT(o.order_id) >= 3 THEN 'Gold'
           WHEN COUNT(o.order_id) = 2 THEN 'Silver'
           ELSE 'Bronze'
       END AS customer_segment
FROM users u
LEFT JOIN orders o ON u.user_id = o.user_id
GROUP BY u.name;

--3. Most Popular Cuisine by Number of Orders
SELECT r.cuisine,
       COUNT(o.order_id) AS total_orders
FROM orders o
JOIN restaurants r ON o.restaurant_id = r.restaurant_id
GROUP BY r.cuisine
ORDER BY total_orders DESC;

--4. Running Total of Revenue (WINDOW)
SELECT DATE(order_date) AS day,
       SUM(total_amount) AS daily_revenue,
       SUM(SUM(total_amount)) OVER (ORDER BY DATE(order_date)) AS running_revenue
FROM orders
WHERE order_status = 'Delivered'
GROUP BY day;

--5. Create a VIEW for Business Reporting
CREATE VIEW restaurant_revenue AS
SELECT r.name,
       SUM(o.total_amount) AS revenue
FROM orders o
JOIN restaurants r ON o.restaurant_id = r.restaurant_id
WHERE o.order_status = 'Delivered'
GROUP BY r.name;

